# Variables declaration
number = 10
Number = 10
greeting = "Hello World!"

# Print variables content
print(number)
print(Number)
print(greeting)

print(36)
print("Greetings!")

# Print variables type
print(type(number))
print(type(greeting))

# Print different variable types using <type> function
print(type(3))
print(type(3.1))
print(type("3"))
print(type("tres"))
print(type(3 == 3))
print(type(('1', '2', '3')))
print(type(["1", "2", "3"]))
print(type({"1", "2", "3"}))
print(type([1, 2, 3]))
